// NameOption
// ****************************************************************************************************************************************
func NameOption(name string) (opt Option) {
	if name != "" {
		return nameOption{name: name}
	}

	return
}

// TypeOption
// ****************************************************************************************************************************************
func TypeOption(tp string) (opt Option) {
	if tp != "" {
		return typeOption{tp: tp}
	}

	return
}

// PathOption
// ****************************************************************************************************************************************
func PathOption(path string) (opt Option) {
	if path != "" {
		return &pathOption{path: path}
	}

	return
}

// SuffixOption SuffixOption("CFG_ENV") => CFG_ENV=prod go run main.go => config-prod
// ****************************************************************************************************************************************
func SuffixOption(varName string) (opt Option) {
	if varName != "" {
		return &suffixOption{varName: varName}
	}

	return
}

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Option
// ****************************************************************************************************************************************
type Option interface {
}

// nameOption *****************************************************************************************************************************
type nameOption struct {
	Option
	name string
}

// typeOption *****************************************************************************************************************************
type typeOption struct {
	Option
	tp string
}

// pathOption *****************************************************************************************************************************
type pathOption struct {
	Option
	path string
}

// suffixOption ***************************************************************************************************************************
type suffixOption struct {
	Option
	varName string
}

