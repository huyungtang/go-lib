// TestGetTags
// ****************************************************************************************************************************************
func TestGetTags(t *testing.T) {
	kk := &struct {
		FieldA string `excel:"999;"`
		FieldB string `excel:"name:abc123;"`
		FieldC string `excel:"-"`
	}{}

	vv := TypeOf(kk)
	for i := 0; i < vv.NumField(); i++ {
		tags := GetTags(vv.Field(i), "excel")
		if tag, isMatch := tags["name"]; isMatch {
			t.Log(tag)
		}
	}

}

