package reflect

import (
	reflect_ "reflect"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// ValueOf
// ****************************************************************************************************************************************
func ValueOf(dto interface{}) reflect_.Value {
	return elemValue(reflect_.ValueOf(dto))
}

// private functions **********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// elemValue ******************************************************************************************************************************
func elemValue(val reflect_.Value) reflect_.Value {
	if val.Type().Kind() == reflect_.Ptr {
		return elemValue(val.Elem())
	}

	return val
}
