package reflect

import (
	reflect_ "reflect"

	"github.com/huyungtang/go-lib/strings"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

const (
	tagSplitStr  = ";"
	tagAssignStr = "="
)

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// GetTags
// ****************************************************************************************************************************************
func GetTags(field reflect_.StructField, tag string) map[string]string {
	m := make(map[string]string)
	tags, isOk := field.Tag.Lookup(tag)
	switch {
	case tags == "-":
		m["ignore"] = "true"
	case isOk:
		ss := strings.Split(tags, tagSplitStr, true)
		for i := 0; i < len(ss); i++ {
			s := strings.Split(ss[i]+"=", tagAssignStr, false)
			m[strings.Lower(s[0])] = s[1]
		}
	}

	return m
}

