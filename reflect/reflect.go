// private functions **********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// unPointer ******************************************************************************************************************************
func unPointer(tp base.Type) base.Type {
	if tp.Kind() == base.Ptr {
		return unPointer(tp.Elem())
	}

	return tp
}

// elemValue ******************************************************************************************************************************
func elemValue(val base.Value) base.Value {
	if val.Type().Kind() == base.Ptr {
		return elemValue(val.Elem())
	}

	return val
}
