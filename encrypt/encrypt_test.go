// ****************************************************************************************************************************************
func TestBCrypt(t *testing.T) {
	str := "Hello World"
	hash, err := BCrypt(str, 4)
	if err != nil {
		t.Error(err)
	}

	if err = BCryptVerify(hash, str); err != nil {
		t.Error(err)
	}
}

