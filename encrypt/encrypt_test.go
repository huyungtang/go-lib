// TestEncrypt
// ****************************************************************************************************************************************
func TestEncrypt(t *testing.T) {
	str := "Hello World"
	hash, err := Encrypt(str, 0)
	if err != nil {
		t.Error(err)
	}

	hash, err = Decrypt(hash)
	if err != nil {
		t.Error(err)
	}

	if str != hash {
		t.Fail()
	}
}

