package reflects

import (
	base "reflect"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// ValueOf
// ****************************************************************************************************************************************
func ValueOf(dto interface{}) base.Value {
	return elemValue(base.ValueOf(dto))
}

// private functions **********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// elemValue ******************************************************************************************************************************
func elemValue(val base.Value) base.Value {
	if val.Type().Kind() == base.Ptr {
		return elemValue(val.Elem())
	}

	return val
}
