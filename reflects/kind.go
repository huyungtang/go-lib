// IsPointer
// ****************************************************************************************************************************************
func IsPointer(elem interface{}) bool {
	return base.TypeOf(elem).Kind() == base.Ptr
}

// KindOf never return reflect.Ptr
// ****************************************************************************************************************************************
func KindOf(elem interface{}) base.Kind {
	tp := unPointer(base.TypeOf(elem))

	return tp.Kind()
}

