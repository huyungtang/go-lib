package reflects

import (
	base "reflect"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// IsPointer ******************************************************************************************************************************
// ****************************************************************************************************************************************
func IsPointer(elem interface{}) bool {
	return base.TypeOf(elem).Kind() == base.Ptr
}

// KindOf *********************************************************************************************************************************
// ****************************************************************************************************************************************
func KindOf(elem interface{}) base.Kind {
	tp := unPointer(base.TypeOf(elem))

	return tp.Kind()
}

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// private functions **********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// unPointer ******************************************************************************************************************************
func unPointer(tp base.Type) base.Type {
	if tp.Kind() == base.Ptr {
		return unPointer(tp.Elem())
	}

	return tp
}
