package reflects

import base "reflect"

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// TypeOf
// ****************************************************************************************************************************************
func TypeOf(elem interface{}) base.Type {
	return unPointer(base.TypeOf(elem))
}

// private functions **********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// unPointer ******************************************************************************************************************************
func unPointer(tp base.Type) base.Type {
	if tp.Kind() == base.Ptr {
		return unPointer(tp.Elem())
	}

	return tp
}
