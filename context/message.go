package context

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

const (
	MessageKindInfo  messageKind = 1
	MessageKindWarn  messageKind = 5
	MessageKindError messageKind = 9
)

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// message ********************************************************************************************************************************
type message struct {
	kind    messageKind
	content string
}

// Message
// ****************************************************************************************************************************************
type Message interface {
	Kind() messageKind
	Content() string
}

// Kind
// ****************************************************************************************************************************************
func (o *message) Kind() messageKind {
	return o.kind
}

// Content
// ****************************************************************************************************************************************
func (o *message) Content() string {
	return o.content
}

// messageKind ****************************************************************************************************************************
type messageKind = uint64

