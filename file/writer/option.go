package writer

import (
	"os"

	"golang.org/x/text/encoding"
	"golang.org/x/text/encoding/traditionalchinese"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

var (
	Big5 Option = &encodingOption{enc: traditionalchinese.Big5}

	Append   Option = &modeOption{mode: os.O_CREATE | os.O_WRONLY | os.O_APPEND}
	Override Option = &modeOption{mode: os.O_CREATE | os.O_WRONLY | os.O_TRUNC}
)

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Option
// ****************************************************************************************************************************************
type Option interface {
	Option()
}

// option *********************************************************************************************************************************
type option int

// option
// ****************************************************************************************************************************************
func (o *option) Option() {
}

// encodingOption *************************************************************************************************************************
type encodingOption struct {
	option
	enc encoding.Encoding
}

// encoder ********************************************************************************************************************************
func (o *encodingOption) encoder() *encoding.Encoder {
	return o.enc.NewEncoder()
}

// modeOption *****************************************************************************************************************************
type modeOption struct {
	option
	mode int
}

