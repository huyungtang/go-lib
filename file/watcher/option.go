package watcher

import (
	"regexp"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// SubscribeOption
// ****************************************************************************************************************************************
func SubscribeOption(evts watcherEvent) Option {
	return &subscribeOption{events: evts}
}

// PatternOption
// ****************************************************************************************************************************************
func PatternOption(pattern string) Option {
	opt := new(patternOption)
	opt.regex = regexp.MustCompile(pattern)

	return opt
}

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Option
// ****************************************************************************************************************************************
type Option interface {
	Option()
}

// option *********************************************************************************************************************************
type option int

// option
// ****************************************************************************************************************************************
func (o *option) Option() {
}

// subscribeOption ************************************************************************************************************************
type subscribeOption struct {
	option
	events watcherEvent
}

// patternOption **************************************************************************************************************************
type patternOption struct {
	option
	regex *regexp.Regexp
}

