// Home
// ****************************************************************************************************************************************
func Home(dirs ...string) string {
	home, _ := os.UserHomeDir()

	return Join(home, dirs...)
}

// Temp
// ****************************************************************************************************************************************
func Temp(dirs ...string) string {

	return Join(os.TempDir(), dirs...)
}

// Join
// ****************************************************************************************************************************************
func Join(root string, dirs ...string) string {
	if root == "" {
		root = os.Args[0]
	}

	if root[len(root)-1:] == "/" {
		root = filepath.Dir(root)
	}

	ps := append([]string{root}, dirs...)

	return base.Clean(base.Join(ps...))
}

// IsFileExists
// ****************************************************************************************************************************************
func IsFileExists(path string) bool {
	if fi, err := os.Stat(path); err != nil || os.IsNotExist(err) || fi.IsDir() {
		return false
	}

	return true
}

