// Init
// ****************************************************************************************************************************************
func Init(fn string, opts ...file.Option) (rdr Reader, err error) {
	if !path.IsFileExists(fn) {
		return nil, file.ErrFileNotFound
	}

	r := new(reader)
	if r.file, err = os.OpenFile(fn, os.O_RDONLY, os.ModePerm); err != nil {
		return
	}

	for i := 0; i < len(opts); i++ {
		switch opt := opts[i].(type) {
		case *file.EncodingOption:
			r.encoding = opt
		}
	}

	return r, nil
}

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Reader
// ****************************************************************************************************************************************
type Reader interface {
	Close() error

	Read() (<-chan *readerDTO, error)
	ReadAll() ([]byte, error)
}

// reader *********************************************************************************************************************************
type reader struct {
	file     *os.File
	encoding *file.EncodingOption
}

// Close
// ****************************************************************************************************************************************
func (o *reader) Close() (err error) {
	if o.file == nil {
		return
	}

	return o.file.Close()
}

// Read
// ****************************************************************************************************************************************
func (o *reader) Read() (ch <-chan *readerDTO, err error) {
	rtn := make(chan *readerDTO)

	go func(r io.Reader, c chan<- *readerDTO) {
		s := bufio.NewScanner(r)
		ln := 0

	LOOP:
		for {
			if isOK := s.Scan(); !isOK {
				c <- &readerDTO{IsEOF: true}
				break LOOP
			}
			ln++
			c <- &readerDTO{Line: ln, Data: s.Text()}
		}
	}(o.getReader(), rtn)

	return rtn, nil
}

// ReadAll
// ****************************************************************************************************************************************
func (o *reader) ReadAll() (bs []byte, err error) {
	if bs, err = ioutil.ReadAll(o.getReader()); err != nil {
		return
	}

	return
}

// getReader ******************************************************************************************************************************
func (o *reader) getReader() io.Reader {
	o.file.Seek(0, 0)

	if o.encoding != nil {
		return transform.NewReader(o.file, o.encoding.Decoder())
	}

	return o.file
}

// readerDTO ******************************************************************************************************************************
type readerDTO struct {
	Line  int
	Data  string
	IsEOF bool
}

