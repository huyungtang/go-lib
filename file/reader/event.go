package reader

import (
	"fmt"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

const (
	Read readerEvent = 1 << iota
	EOF
	Error
)

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// ReaderEvent
// ****************************************************************************************************************************************
type ReaderEvent struct {
	Event  readerEvent
	LineNO uint
	String string
	Err    error
}

// Parse
// ****************************************************************************************************************************************
func (o *ReaderEvent) Parse(format string, args ...interface{}) (err error) {
	_, err = fmt.Sscanf(o.String, format, args...)

	return
}

// readerEvent ****************************************************************************************************************************
type readerEvent uint32

