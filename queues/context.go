// ContextHandler
// ****************************************************************************************************************************************
type ContextHandler func(*Context) error

// Context
// ****************************************************************************************************************************************
type Context struct {
	Exchange string
	Routing  string
	Body     []byte
	idx      int
	handlers []ContextHandler
	err      error
}

// Next
// ****************************************************************************************************************************************
func (o *Context) Next() {
	o.idx++
	if o.idx >= len(o.handlers) || o.handlers[o.idx] == nil {
		return
	}

	if err := o.handlers[o.idx](o); err != nil {
		o.err = err
		return
	}
}

// SetHandler
// ****************************************************************************************************************************************
func (o *Context) SetHandler(hs []ContextHandler) *Context {
	o.idx = -1
	o.handlers = hs

	return o
}

// Err
// ****************************************************************************************************************************************
func (o *Context) Err() (err error) {
	return o.err
}

