// ContextHandler
// ****************************************************************************************************************************************
type ContextHandler func(*Context) error

// Context
// ****************************************************************************************************************************************
type Context struct {
	Exchange string
	Routing  string
	Body     []byte
	idx      int
	handlers []ContextHandler
	err      error
}

// Next
// ****************************************************************************************************************************************
func (o *Context) Next() {
	o.idx++
	if o.idx < len(o.handlers) && o.handlers[o.idx] != nil {
		o.err = o.handlers[o.idx](o)
	}
}

// SetHandler
// ****************************************************************************************************************************************
func (o *Context) SetHandler(hs []ContextHandler) *Context {
	o.idx = -1
	o.handlers = hs

	return o
}

// SetError
// ****************************************************************************************************************************************
func (o *Context) SetError(e error) *Context {
	o.err = e

	return o
}

// Err
// ****************************************************************************************************************************************
func (o *Context) Err() (err error) {
	return o.err
}

