// context ********************************************************************************************************************************
type context struct {
	exg string
	rut string
	bdy []byte
	err error
	hdl []queues.ContextHandler
	val map[string]interface{}
}

// Deadline
// ****************************************************************************************************************************************
func (o *context) Deadline() (deadline time.Time, ok bool) {
	return
}

// Done
// ****************************************************************************************************************************************
func (o *context) Done() (ch <-chan struct{}) {
	return
}

// Err
// ****************************************************************************************************************************************
func (o *context) Err() (err error) {
	return o.err
}

// Value
// ****************************************************************************************************************************************
func (o *context) Value(key interface{}) interface{} {
	return nil
}

// Exchange
// ****************************************************************************************************************************************
func (o *context) Exchange() string {
	return o.exg
}

// Routing
// ****************************************************************************************************************************************
func (o *context) Routing() string {
	return o.rut
}

// Body
// ****************************************************************************************************************************************
func (o *context) Body() []byte {
	return o.bdy
}

// Next
// ****************************************************************************************************************************************
func (o *context) Next() {
	if len(o.hdl) == 0 || o.hdl[0] == nil {
		return
	}

	var hdl queues.ContextHandler
	hdl, o.hdl = o.hdl[0], o.hdl[1:]

	if err := hdl(o); err != nil {
		o.err = err
		return
	}
}

// Set
// ****************************************************************************************************************************************
func (o *context) Set(key string, val interface{}) {
	if o.val == nil {
		o.val = make(map[string]interface{})
	}
	o.val[key] = val
}

// Get
// ****************************************************************************************************************************************
func (o *context) Get(key string) interface{} {
	return o.val[key]
}

// exec ***********************************************************************************************************************************
func (o *context) exec() {
	o.Next()
}

