// Context
// ****************************************************************************************************************************************
type Context struct {
	msg      *amqp.Delivery
	idx      int
	handlers []func(*Context) error
	err      error
}

// Next
// ****************************************************************************************************************************************
func (o *Context) Next() {
	o.idx++
	if o.idx < len(o.handlers) && o.handlers[o.idx] != nil {
		o.err = o.handlers[o.idx](o)
	}
}

// Body
// ****************************************************************************************************************************************
func (o *Context) Body() []byte {
	return o.msg.Body
}

// Err
// ****************************************************************************************************************************************
func (o *Context) Err() (err error) {
	return o.err
}

