// context ********************************************************************************************************************************
type context struct {
	exg string
	rut string
	bdy []byte
	err error
	idx int
	hdl []queues.ContextHandler
}

// Deadline
// ****************************************************************************************************************************************
func (o *context) Deadline() (deadline time.Time, ok bool) {
	return
}

// Done
// ****************************************************************************************************************************************
func (o *context) Done() (ch <-chan struct{}) {
	return
}

// Err
// ****************************************************************************************************************************************
func (o *context) Err() (err error) {
	return o.err
}

// Value
// ****************************************************************************************************************************************
func (o *context) Value(key interface{}) interface{} {
	return nil
}

// Exchange
// ****************************************************************************************************************************************
func (o *context) Exchange() string {
	return o.exg
}

// Routing
// ****************************************************************************************************************************************
func (o *context) Routing() string {
	return o.rut
}

// Body
// ****************************************************************************************************************************************
func (o *context) Body() []byte {
	return o.bdy
}

// Next
// ****************************************************************************************************************************************
func (o *context) Next() {
	o.idx++
	if o.idx >= len(o.hdl) || o.hdl[o.idx] == nil {
		return
	}

	if err := o.hdl[o.idx](o); err != nil {
		o.err = err
		return
	}
}

// exec ***********************************************************************************************************************************
func (o *context) exec() {
	o.idx = -1
	o.Next()
}

