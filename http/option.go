// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// BodyOption
// ****************************************************************************************************************************************
func BodyOption(key, val string) Option {
	return &dataOption{key: key, val: val}
}

// HandlerOption
// ****************************************************************************************************************************************
func HandlerOption(hdl contextHandler) Option {
	return &handlerOption{hdl: hdl}
}

// HeaderOption
// ****************************************************************************************************************************************
func HeaderOption(key, val string) Option {
	return &headerOption{key: key, value: val}
}

// ContentOption
// ****************************************************************************************************************************************
func ContentOption(val string) Option {
	return &contentOption{value: val}
}

// ParamOption
// ****************************************************************************************************************************************
func ParamOption(key, val string) Option {
	return &paramOption{key: key, value: val}
}

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Option
// ****************************************************************************************************************************************
type Option interface {
	Option()
}

// option *********************************************************************************************************************************
type option int

// option
// ****************************************************************************************************************************************
func (o *option) Option() {
}

// dataOption *****************************************************************************************************************************
type dataOption struct {
	option
	key string
	val string
}

// handlerOption **************************************************************************************************************************
type handlerOption struct {
	option
	hdl contextHandler
}

// headerOption ***************************************************************************************************************************
type headerOption struct {
	option
	key   string
	value string
}

// contentOption **************************************************************************************************************************
type contentOption struct {
	option
	value string
}

// paramOption ****************************************************************************************************************************
type paramOption struct {
	option
	key   string
	value string
}

