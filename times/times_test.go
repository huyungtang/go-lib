// TestParse
// ****************************************************************************************************************************************
func TestParse(t *testing.T) {
	dt := "2006-01-02 15:04:05"
	tm, err := Parse(DateFmt, dt)
	if err != nil {
		t.Error(err)
	} else if tm.Local().UnixMilli() != 1136185445000 {
		t.Fail()
	}
}

