// beforeCreate ***************************************************************************************************************************
func beforeCreate(dto interface{}) {
	if id, isOK := dto.(IIdentity); isOK {
		id.NewID()
	}

	if c, isOK := dto.(dbs.ICreatedTime); isOK {
		c.Create()
	}

	beforeUpdate(dto)
}

// beforeUpdate ***************************************************************************************************************************
func beforeUpdate(dto interface{}) {
	if u, isOK := dto.(dbs.IUpdatedTime); isOK {
		u.Update()
	}
}
