// IIdentity
// ****************************************************************************************************************************************
type IIdentity interface {
	NewID()
}

// Identity
// ****************************************************************************************************************************************
type Identity struct {
	ID primitive.ObjectID `bson:"_id,omitempty"`
}

// NewID
// ****************************************************************************************************************************************
func (o *Identity) NewID() {
	o.ID = primitive.NewObjectID()
}

