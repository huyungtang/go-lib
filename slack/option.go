// AsUserOption
// ****************************************************************************************************************************************
func AsUserOption(user string) Options {
	return func(s *client) {
		s.msgOpts = append(s.msgOpts,
			base.MsgOptionAsUser(true),
			base.MsgOptionUser(user),
		)
	}
}

