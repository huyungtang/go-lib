// UnixDay
// ****************************************************************************************************************************************
type UnixDay int64

// UnixMilli
// ****************************************************************************************************************************************
func (o UnixDay) UnixMilli() int64 {
	return times.UnixDay(int64(o)).UnixMilli()
}

// SetMilli
// ****************************************************************************************************************************************
func (o *UnixDay) SetMilli(ms int64) {
	*o = UnixDay(times.Unix(ms/1000, 0).UnixDay())
}

