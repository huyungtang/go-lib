// UnixDay
// ****************************************************************************************************************************************
type UnixDay int64

// UnixMilli
// ****************************************************************************************************************************************
func (o UnixDay) UnixMilli() int64 {
	return times.FromUnixDay(int64(o)).UnixMilli()
}

// SetMilli
// ****************************************************************************************************************************************
func (o *UnixDay) SetMilli(ms int64) {
	*o = UnixDay(times.UnixDay(times.FromUnix(ms/1000, 0)))
}

