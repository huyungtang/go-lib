// Tables
// ****************************************************************************************************************************************
type Tables interface {
	// ↓↓↓↓↓↓↓↓↓↓ Crud / Create *************************************************************************************************************
	// ↑↑↑↑↑↑↑↑↑↑ Crud / Create *************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ cRud / Query **************************************************************************************************************
	Get(interface{}) error
	// ↑↑↑↑↑↑↑↑↑↑ cRud / Query **************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ crUd / Update *************************************************************************************************************
	// ↑↑↑↑↑↑↑↑↑↑ crUd / Update *************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ cruD / Delete *************************************************************************************************************
	// ↑↑↑↑↑↑↑↑↑↑ cruD / Delete *************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ statements ****************************************************************************************************************
	Where(query interface{}, args ...interface{}) Tables
	OrderBy(...string) Tables
	Offset(int) Tables
	Limit(int) Tables
	// ↑↑↑↑↑↑↑↑↑↑ statements ****************************************************************************************************************
}

// Table
// ****************************************************************************************************************************************
type Table struct {
	db *base.DB
}

// SetContext
// ****************************************************************************************************************************************
func (o *Table) SetContext(ctx interface{}) {
	o.db = ctx.(*base.DB)
}

// Crud / Create
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Create
// ****************************************************************************************************************************************
func (o *Table) Create(dto interface{}) (err error) {
	switch reflect.KindOf(dto) {
	case reflect_.Struct:
		beforeCreate(dto)
		err = o.db.Create(dto).Error
	case reflect_.Slice:
		val := reflect.ValueOf(dto)
		dtos := make([]interface{}, val.Len())
		for i := 0; i < val.Len(); i++ {
			dtos[i] = val.Index(i).Interface()
			beforeCreate(dtos[i])
		}
		err = o.db.Create(dto).Error
	}

	return
}

// cRud / Query
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Get
// ****************************************************************************************************************************************
func (o *Table) Get(dto interface{}) (err error) {
	if p, isOK := dto.(db.Paged); isOK {
		var recs int64
		if err = o.db.Count(&recs).Error; err != nil {
			return
		}
		p.Count(recs)
		err = o.db.Offset(p.Offset()).Limit(p.PageSize()).Find(p.GetData()).Error
	} else if reflect.KindOf(dto) == reflect_.Struct {
		err = o.db.Take(dto).Error
	} else {
		err = o.db.Find(dto).Error
	}

	return
}

// GetByID
// ****************************************************************************************************************************************
func (o *Table) GetByID(dto interface{}, id interface{}) (err error) {
	result := o.db.Where("id = ?", id).Take(dto)

	if err = result.Error; err != nil {
		return
	}

	if result.RowsAffected != 1 {
		return db.ErrRecordNotFound
	}

	return
}

// crUd / Update
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// cruD / Delete
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// statements
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Where
// ****************************************************************************************************************************************
func (o *Table) Where(query interface{}, args ...interface{}) Tables {
	o.db = o.db.Where(query, args...)

	return o
}

// OrderBy
// ****************************************************************************************************************************************
func (o *Table) OrderBy(cols ...string) Tables {
	for i := 0; i < len(cols); i++ {
		switch cols[i][0] {
		case 0x2b:
			o.db = o.db.Order(cols[i][1:])
		case 0x2d:
			o.db = o.db.Order(strings.Format("%s DESC", cols[i][1:]))
		default:
			o.db = o.db.Order(cols[i])
		}
	}

	return o
}

// Offset
// ****************************************************************************************************************************************
func (o *Table) Offset(num int) Tables {
	o.db = o.db.Offset(num)

	return o
}

// Limit
// ****************************************************************************************************************************************
func (o *Table) Limit(num int) Tables {
	o.db = o.db.Limit(num)

	return o
}

// private functions **********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// beforeCreate ***************************************************************************************************************************
func beforeCreate(dto interface{}) {
	if c, isOK := dto.(db.CreateTime); isOK {
		c.Create()
	}

	beforeUpdate(dto)
}

// beforeUpdate ***************************************************************************************************************************
func beforeUpdate(dto interface{}) {
	if u, isOK := dto.(db.UpdateTime); isOK {
		u.Update()
	}
}
