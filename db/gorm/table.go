package gorm

import (
	"errors"
	"reflect"

	"github.com/huyungtang/go-lib/db"
	"github.com/huyungtang/go-lib/reflects"
	"github.com/huyungtang/go-lib/strings"
	"gorm.io/gorm"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

var errNotFound = errors.New("not found")

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// GormTable ******************************************************************************************************************************
// ****************************************************************************************************************************************
type GormTable struct {
	db *gorm.DB
}

// SetContext *****************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) SetContext(ctx interface{}) {
	o.db = ctx.(*gorm.DB)
}

// Crud / Create **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Create *********************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) Create(dto interface{}) (err error) {
	return o.db.Create(dto).Error
}

// cRud / Query ***************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Get ************************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) Get(dto interface{}) (err error) {
	if p, isOK := dto.(db.IPaged); isOK {
		var recs int64
		if err = o.db.Count(&recs).Error; err != nil {
			return
		}
		p.Count(recs)
		err = o.db.Offset(p.Offset()).Limit(p.PageSize()).Find(p.GetData()).Error
	} else if reflects.KindOf(dto) == reflect.Struct {
		err = o.db.Take(dto).Error
	} else {
		err = o.db.Find(dto).Error
	}

	return
}

// GetByID ********************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) GetByID(dto interface{}, id interface{}) (err error) {
	result := o.db.Where("id = ?", id).Take(dto)

	if err = result.Error; err != nil {
		return
	}

	if result.RowsAffected != 1 {
		return errNotFound
	}

	return
}

