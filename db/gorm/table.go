// Where **********************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) Where(query interface{}, args ...interface{}) db.ITable {
	o.db = o.db.Where(query, args...)

	return o
}

// OrderBy ********************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) OrderBy(cols ...string) db.ITable {
	for i := 0; i < len(cols); i++ {
		switch cols[i][0:1] {
		case "+":
			o.db = o.db.Order(cols[i][1:])
		case "-":
			o.db = o.db.Order(strings.Format("%s DESC", cols[i][1:]))
		default:
			o.db = o.db.Order(cols[i])
		}
	}

	return o
}

// Offset *********************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) Offset(num int) db.ITable {
	o.db = o.db.Offset(num)

	return o
}

// Limit **********************************************************************************************************************************
// ****************************************************************************************************************************************
func (o *GormTable) Limit(num int) db.ITable {
	o.db = o.db.Limit(num)

	return o
}

