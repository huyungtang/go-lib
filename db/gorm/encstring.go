// EncStr
// ****************************************************************************************************************************************
type EncStr string

// Scan
// ****************************************************************************************************************************************
func (o *EncStr) Scan(val interface{}) (err error) {
	var s string
	if s, err = decryptString(val); err != nil {
		return
	}

	*o = EncStr(s)

	return
}

// Value
// ****************************************************************************************************************************************
func (o EncStr) Value() (val driver.Value, err error) {
	return encryptString(o.String())
}

// Set
// ****************************************************************************************************************************************
func (o *EncStr) Set(s string) {
	*o = EncStr(s)
}

// String
// ****************************************************************************************************************************************
func (o EncStr) String() string {
	return string(o)
}

// private functions **********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// decryptString **************************************************************************************************************************
func decryptString(val interface{}) (s string, err error) {
	var str string
	var isOk bool
	if str, isOk = val.(string); !isOk {
		var bs []byte
		if bs, isOk = val.([]byte); !isOk {
			return "", errInvalidData
		}

		if len(bs) == 0 {
			return
		}

		str = string(bs)
	}

	s = strings.Format("$a%d$%s", encrypt.DefaCost, str)
	if s, err = encrypt.Decrypt(s); err != nil {
		return
	}

	return
}

// encryptString **************************************************************************************************************************
func encryptString(s string) (val driver.Value, err error) {
	val = ""
	if len(s) > 0 {
		if s, err = encrypt.Encrypt(s, encrypt.DefaCost); err != nil {
			return
		}

		val = s[5:]
	}

	return
}
