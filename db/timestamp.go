// CreateTime
// ****************************************************************************************************************************************
type CreateTime interface {
	Create()
}

// CreatedTime
// ****************************************************************************************************************************************
type CreatedTime struct {
	CreatedTime int64 `bson:"created_time,omitempty," gorm:"column:created_time;"`
}

// Create
// ****************************************************************************************************************************************
func (o *CreatedTime) Create() {
	o.CreatedTime = time.UnixMilli()
}

// UpdateTime
// ****************************************************************************************************************************************
type UpdateTime interface {
	Update()
}

// UpdatedTime
// ****************************************************************************************************************************************
type UpdatedTime struct {
	UpdatedTime int64 `bson:"updated_time,omitempty," gorm:"column:updated_time;"`
}

// Update
// ****************************************************************************************************************************************
func (o *UpdatedTime) Update() {
	o.UpdatedTime = time.UnixMilli()
}

