// Paged
// ****************************************************************************************************************************************
type Paged interface {
	Offset() int
	PageSize() int
	GetData() interface{}
	Count(int64)
}

// PagedDTO
// ****************************************************************************************************************************************
type PagedDTO struct {
	Page    int
	Size    int
	Records int64
	Data    interface{}
}

// Offset
// ****************************************************************************************************************************************
func (o *PagedDTO) Offset() int {
	return o.Page * o.Size
}

// PageSize
// ****************************************************************************************************************************************
func (o *PagedDTO) PageSize() int {
	return o.Size
}

// GetData
// ****************************************************************************************************************************************
func (o *PagedDTO) GetData() interface{} {
	return o.Data
}

// Count
// ****************************************************************************************************************************************
func (o *PagedDTO) Count(recs int64) {
	o.Records = recs
}

