// Sort
// ****************************************************************************************************************************************
func (o *Collection) Sort(cols ...string) Collections {
	sort := primitive.D{}
	for i := 0; i < len(cols); i++ {
		switch cols[i][0] {
		case 0x2b:
			sort = append(sort, primitive.E{Key: cols[i][1:], Value: 1})
		case 0x2d:
			sort = append(sort, primitive.E{Key: cols[i][1:], Value: -1})
		default:
			sort = append(sort, primitive.E{Key: cols[i], Value: 1})
		}
	}
	o.opts = append(o.opts, options.Find().SetSort(sort))

	return o
}

// Skip
// ****************************************************************************************************************************************
func (o *Collection) Skip(i int64) Collections {
	o.opts = append(o.opts, options.Find().SetSkip(i))

	return o
}

// Limit
// ****************************************************************************************************************************************
func (o *Collection) Limit(i int64) Collections {
	o.opts = append(o.opts, options.Find().SetLimit(i))

	return o
}

