// Collections
// ****************************************************************************************************************************************
type Collections interface {
	// ↓↓↓↓↓↓↓↓↓↓ Crud / Create *************************************************************************************************************
	// ↑↑↑↑↑↑↑↑↑↑ Crud / Create *************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ cRud / Query **************************************************************************************************************
	Find(dto, filter interface{}) error
	// ↑↑↑↑↑↑↑↑↑↑ cRud / Query **************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ crUd / Update *************************************************************************************************************
	// ↑↑↑↑↑↑↑↑↑↑ crUd / Update *************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ cruD / Delete *************************************************************************************************************
	// ↑↑↑↑↑↑↑↑↑↑ cruD / Delete *************************************************************************************************************

	// ↓↓↓↓↓↓↓↓↓↓ statements ****************************************************************************************************************
	Sort(...string) Collections
	Skip(int64) Collections
	Limit(int64) Collections
	// ↑↑↑↑↑↑↑↑↑↑ statements ****************************************************************************************************************
}

// Collection
// ****************************************************************************************************************************************
type Collection struct {
	db *mongo_.Collection

	opts []*options.FindOptions
}

// SetContext
// ****************************************************************************************************************************************
func (o *Collection) SetContext(ctx interface{}) {
	o.db = ctx.(*mongo_.Collection)
	o.opts = make([]*options.FindOptions, 0)
}

// Crud / Create
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Create
// ****************************************************************************************************************************************
func (o *Collection) Create(dto interface{}) (err error) {
	switch reflect.KindOf(dto) {
	case reflect_.Map:
		_, err = o.db.InsertOne(context.TODO(), dto)
	case reflect_.Struct:
		beforeCreate(dto)
		_, err = o.db.InsertOne(context.TODO(), dto)
	case reflect_.Slice:
		vals := reflect.ValueOf(dto)
		dtos := make([]interface{}, vals.Len())
		for i := 0; i < vals.Len(); i++ {
			dtos[i] = vals.Index(i).Interface()
			beforeCreate(dtos[i])
		}
		_, err = o.db.InsertMany(context.TODO(), dtos)
	}

	return
}

// cRud / Query
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Find
// ****************************************************************************************************************************************
func (o *Collection) Find(dto interface{}, fileter interface{}) (err error) {
	var tar interface{}
	if p, isOK := dto.(db.Paged); isOK {
		var recs int64
		if recs, err = o.db.CountDocuments(context.TODO(), fileter); err != nil {
			return
		}
		p.Count(recs)
		o.Skip(int64(p.Offset())).Limit(int64(p.PageSize()))
		tar = p.GetData()
	} else if reflect.KindOf(dto) == reflect_.Struct {
		o.Limit(1)
	} else {
		tar = dto
	}

	var res *mongo_.Cursor
	if res, err = o.db.Find(context.TODO(), fileter, o.opts...); err != nil {
		return
	}

	if tar == nil {
		if res.Next(context.TODO()) {
			return res.Decode(dto)
		}

		return db.ErrRecordNotFound
	}

	return res.All(context.TODO(), tar)
}

// GetByID
// ****************************************************************************************************************************************
func (o *Collection) GetByID(dto interface{}, id interface{}) (err error) {
	switch oid := id.(type) {
	case string:
		if id, err = primitive.ObjectIDFromHex(oid); err != nil {
			return
		}
	}

	return o.db.FindOne(context.TODO(), primitive.M{"_id": id}).Decode(dto)
}

