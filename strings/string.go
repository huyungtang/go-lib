package strings

import (
	"fmt"
	"regexp"
	strings_ "strings"

	"github.com/huyungtang/go-lib/number"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

var (
	randomChars = []rune("ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789zyxwvutsrqponmlkjihgfedcba")
)

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// EmitEmpty
// ****************************************************************************************************************************************
func EmitEmpty(strs []string) []string {
	for i := len(strs) - 1; i >= 0; i-- {
		if strs[i] == "" {
			strs = append(strs[0:i], strs[i+1:]...)
		}
	}

	return strs
}

// Find
// ****************************************************************************************************************************************
func Find(pattern, str string) string {
	return regexp.MustCompile(pattern).FindString(str)
}

// Format
// ****************************************************************************************************************************************
func Format(format string, args ...interface{}) string {
	return fmt.Sprintf(format, args...)
}

// Join
// ****************************************************************************************************************************************
func Join(sep string, isEmitEmpty bool, strs ...string) string {
	if isEmitEmpty {
		strs = EmitEmpty(strs)
	}

	return strings_.Join(strs, sep)
}

// Random
// ****************************************************************************************************************************************
func Random(size int) string {
	b := make([]rune, size)
	m := len(randomChars) - 1
	for i := 0; i < size; i++ {
		b[i] = randomChars[number.Random(0, m)]
	}

	return string(b)
}

// Reverse
// ****************************************************************************************************************************************
func Reverse(str string) string {
	r := []rune(str)
	for i, j := 0, len(r)-1; i < len(r)/2; i, j = i+1, j-1 {
		r[i], r[j] = r[j], r[i]
	}

	return string(r)
}

// Split
// ****************************************************************************************************************************************
func Split(str, sep string, isEmitEmpty bool) (strs []string) {
	strs = strings_.Split(str, sep)
	if isEmitEmpty {
		strs = EmitEmpty(strs)
	}

	return
}

// Splitln
// ****************************************************************************************************************************************
func Splitln(str string, leng int) (strs []string) {
	strs = make([]string, 0)
	for {
		if len(str) > leng {
			strs = append(strs, str[:leng])
			str = str[leng:]
		} else {
			strs = append(strs, str)
			break
		}
	}

	return
}

