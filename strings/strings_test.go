// TestOmitEmpty
// ****************************************************************************************************************************************
func TestOmitEmpty(t *testing.T) {
	strs := OmitEmpty([]string{"1", "", "2", "3", "", ""})

	if len(strs) != 3 {
		t.Fail()
	}
}

// TestOmitPattern
// ****************************************************************************************************************************************
func TestOmitPattern(t *testing.T) {
	strs := Omit([]string{"1", "a", "2", "3", "b", "c"}, `[^\d]`)

	if len(strs) != 3 {
		t.Fail()
	}
}

// TestParse
// ****************************************************************************************************************************************
func TestParse(t *testing.T) {
	var v1, v2 int
	str := "1|2"
	if err := Parse(str, "%d|%d", &v1, &v2); err != nil {
		t.Error(err)
	}

	if v1 != 1 || v2 != 2 {
		t.Fail()
	}
}

// TestRandom
// ****************************************************************************************************************************************
func TestRandom(t *testing.T) {
	ln := 20
	str := Random(ln)

	if len(str) != ln {
		t.Fail()
	}
}

// TestUUID
// ****************************************************************************************************************************************
func TestUUID(t *testing.T) {
	v := ToUpper(UUID())
	t.Logf("%s%s%s", v[9:13], v[14:18], v[19:23])
}

