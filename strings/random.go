package strings

import "github.com/huyungtang/go-lib/numbers"

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

var (
	randomChars = []rune("ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789zyxwvutsrqponmlkjihgfedcba")
)

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Random
// ****************************************************************************************************************************************
func Random(size int) string {
	b := make([]rune, size)
	m := len(randomChars) - 1
	for i := 0; i < size; i++ {
		b[i] = randomChars[numbers.Random(0, m)]
	}

	return string(b)
}

