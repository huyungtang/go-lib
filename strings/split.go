// Join
// ****************************************************************************************************************************************
func Join(sep string, emitEmpty bool, strs ...string) string {
	if emitEmpty {
		for i := len(strs) - 1; i >= 0; i-- {
			if strs[i] == "" {
				strs = append(strs[0:i], strs[i+1:]...)
			}
		}
	}

	return base.Join(strs, sep)
}

// Split
// ****************************************************************************************************************************************
func Split(str, sep string, emitEmpty bool) (strs []string) {
	strs = base.Split(str, sep)
	if emitEmpty {
		for i := len(strs) - 1; i >= 0; i-- {
			if strs[i] == "" {
				strs = append(strs[0:i], strs[i+1:]...)
			}
		}
	}

	return

}

