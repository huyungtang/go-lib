package numbers

import (
	"math/rand"

	"github.com/huyungtang/go-lib/times"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Random
// ****************************************************************************************************************************************
func Random(mi, mx int) int {
	rand.Seed(times.UnixMicro())

	return rand.Intn(mx-mi) + mi
}

