// TestMax
// ****************************************************************************************************************************************
func TestMax(t *testing.T) {
	if Max(3, 2, -6, 4, 5, 10.2, 1, 7, -8, 9, 0) != 10.2 {
		t.Fail()
	}
}

// TestMin
// ****************************************************************************************************************************************
func TestMin(t *testing.T) {
	if Min(3, 2, -6, 4, 5, 1, -7, 8, 9, 0) != -7 {
		t.Fail()
	}
}

// TestRand
// ****************************************************************************************************************************************
func TestRand(t *testing.T) {
	mi, mx := 20, 100
	rn := Rand(mi, mx)
	for i := 0; i < 10; i++ {
		if r := rn.NextInt(); r < mi || r > mx {
			t.Fail()
			break
		}
	}
}

