package number

import (
	"math/rand"

	"github.com/huyungtang/go-lib/time"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Random
// ****************************************************************************************************************************************
func Random(mi, mx int) int {
	r := rand.New(rand.NewSource(time.UnixNano()))

	return r.Intn(mx-mi) + mi
}

