package number

import (
	"math/rand"
	"sync"

	"github.com/huyungtang/go-lib/times"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

var (
	mute sync.Mutex
)

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Random
// ****************************************************************************************************************************************
func Random(mi, mx int) int {
	mute.Lock()
	rand.Seed(times.UnixMicro())
	mute.Unlock()

	return rand.Intn(mx-mi) + mi
}

