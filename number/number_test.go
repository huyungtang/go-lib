// TestRandom
// ****************************************************************************************************************************************
func TestRandom(t *testing.T) {
	mi, mx := 20, 100

	for i := 0; i < 10; i++ {
		r := Random(mi, mx)
		if r < mi || r > mx {
			t.Fail()
			break
		}
	}
}

