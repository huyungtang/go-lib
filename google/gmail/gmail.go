// Init
// ****************************************************************************************************************************************
func Init(opts ...google.Options) (serv Service, err error) {
	var s *base.Service
	cfg := google.ApplyOptions(opts)
	if s, err = base.NewService(context.Background(), cfg.GetClientOption()); err != nil {
		return
	}

	return &service{s}, nil
}

