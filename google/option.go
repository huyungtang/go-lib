// Option
// ****************************************************************************************************************************************
type Option struct {
	*oauth2.Config
	*oauth2.Token

	AllDay       bool
	CalendarId   string
	Description  string
	Recurrency   []string
	Duration     time.Duration
	Timezone     string
	EndTime      time.Time
	Transparency string

	Header     textproto.MIMEHeader
	Body       []byte
	Attach     map[string][]byte
	AttachOnce sync.Once
}

// ApplyOptions
// ****************************************************************************************************************************************
func (o *Option) ApplyOptions(opts []Options, defa ...Options) (opt *Option) {
	opts = append(defa, opts...)
	for _, optFn := range opts {
		optFn(o)
	}

	return o
}

// GetClientOption
// ****************************************************************************************************************************************
func (o *Option) GetClientOption() option.ClientOption {
	return option.WithHTTPClient(o.Config.Client(context.Background(), o.Token))
}

// Options
// ****************************************************************************************************************************************
type Options func(*Option)

