package gcal

import (
	"github.com/huyungtang/go-lib/google"
	base "google.golang.org/api/calendar/v3"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// type defineds **************************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// result *********************************************************************************************************************************
type result struct {
	*base.Event
	*base.Events
	evts int
	err  error
}

// EventId
// ****************************************************************************************************************************************
func (o *result) EventId() (id string) {
	if o.Event != nil {
		id = o.Event.Id
	}

	return
}

// Err
// ****************************************************************************************************************************************
func (o *result) Err() (err error) {
	return o.err
}

// Len
// ****************************************************************************************************************************************
func (o *result) Len() (c int) {
	return o.evts
}

// Index
// ****************************************************************************************************************************************
func (o *result) Index(i int) (res google.Event) {
	if o.Events != nil && i >= 0 && i < o.evts {
		res = &result{
			Event: o.Events.Items[i],
		}
	}

	return
}

// Next
// ****************************************************************************************************************************************
func (o *result) Next() (tkn string) {
	if o.Events != nil {
		tkn = o.NextPageToken
	}

	return
}

