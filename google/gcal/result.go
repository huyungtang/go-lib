// result *********************************************************************************************************************************
type result struct {
	*base.Event
	err error
}

// EventResult
// ****************************************************************************************************************************************
type EventResult interface {
	EventId() string
	Err() error
}

// EventId
// ****************************************************************************************************************************************
func (o *result) EventId() string {
	return o.Event.Id
}

// Err
// ****************************************************************************************************************************************
func (o *result) Err() (err error) {
	return o.err
}

