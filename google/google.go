// TokenFromWeb
// ****************************************************************************************************************************************
func TokenFromWeb(option Options) {
	cfg := ApplyOptions([]Options{option})
	if cfg.Config == nil {
		return
	}

	authURL := cfg.Config.AuthCodeURL("state-token", oauth2.AccessTypeOffline)
	fmt.Printf("Go to the following link in your browser then type the "+
		"authorization code: \n%v\n", authURL)

	var authCode string
	if _, err := fmt.Scan(&authCode); err != nil {
		log.Fatalf("Unable to read authorization code: %v", err)
	}

	tok, err := cfg.Config.Exchange(context.Background(), authCode)
	if err != nil {
		log.Fatalf("Unable to retrieve token from web: %v", err)
	}

	bs, _ := json.Marshal(tok)
	log.Println(string(bs))
}

