// ParseGlobOption
// ****************************************************************************************************************************************
func ParseGlobOption(pattern string) Options {
	return func(p *tmpl) {
		p.Template, p.err = template.ParseGlob(pattern)
	}
}

// WriterOption
// ****************************************************************************************************************************************
func WriterOption(w io.Writer) Options {
	return func(p *tmpl) {
		p.buf = w
	}
}

