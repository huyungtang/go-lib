// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Unix
// ****************************************************************************************************************************************
func Unix(sec, nsec int64) time_.Time {
	return time_.Unix(sec, nsec)
}

// UnixSecond
// ****************************************************************************************************************************************
func UnixSecond() int64 {
	return time_.Now().Unix()
}

// UnixMilli
// ****************************************************************************************************************************************
func UnixMilli() int64 {
	return time_.Now().UnixMilli()
}

// UnixMicro
// ****************************************************************************************************************************************
func UnixMicro() int64 {
	return time_.Now().UnixMicro()
}

// UnixNano
// ****************************************************************************************************************************************
func UnixNano() int64 {
	return time_.Now().UnixNano()
}

