// Local
// ****************************************************************************************************************************************
func Local(tm time_.Time) time_.Time {
	return time_.Date(tm.Year(), tm.Month(), tm.Day(), tm.Hour(), tm.Minute(), tm.Second(), tm.Nanosecond(), time_.Local)
}

