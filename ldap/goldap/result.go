// result *********************************************************************************************************************************
type result struct {
	*base.SearchResult
	recs int
}

// Len
// ****************************************************************************************************************************************
func (o *result) Len() int {
	return o.recs
}

// Index
// ****************************************************************************************************************************************
func (o *result) Index(i int) (ety ldap.Entity) {
	if i >= 0 && i < o.recs {
		ety = &entity{Entry: o.Entries[i]}
	}

	return
}

