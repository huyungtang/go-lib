// result *********************************************************************************************************************************
type result struct {
	*base.SearchResult
}

// Len
// ****************************************************************************************************************************************
func (o *result) Len() int {
	return len(o.Entries)
}

// Index
// ****************************************************************************************************************************************
func (o *result) Index(i int) ldap.Entity {
	return &entity{Entry: o.Entries[i]}
}

