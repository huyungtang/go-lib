package logger

import (
	"fmt"
	"log"
)

// constants & variables ******************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// public functions ***********************************************************************************************************************
// ****************************************************************************************************************************************
// ****************************************************************************************************************************************

// Fatal
// ****************************************************************************************************************************************
func Fatal(err error) {
	log.Fatal(err)
}

// Log
// ****************************************************************************************************************************************
func Log(args ...interface{}) {
	log.Print(args...)
}

// Logf
// ****************************************************************************************************************************************
func Logf(msg string, args ...interface{}) {
	log.Printf(msg, args...)
}

// Print
// ****************************************************************************************************************************************
func Print(args ...interface{}) {
	fmt.Print(args...)
}

// Printf
// ****************************************************************************************************************************************
func Printf(msg string, args ...interface{}) {
	fmt.Printf(msg, args...)
}

// Println
// ****************************************************************************************************************************************
func Println(args ...interface{}) {
	fmt.Println(args...)
}

